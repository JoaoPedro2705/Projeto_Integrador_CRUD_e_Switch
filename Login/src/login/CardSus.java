    private void salvarCardSusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarCardSusButtonActionPerformed
      String cardSus = cardSusTextArea.getText().trim();

    if (cardSus.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Digite um Cartão do SUS!");
        return;
    }

    try {
        
        InfoDAO dao = new InfoDAO();
    dao.inserirCardSus(cardSus);

    FileWriter fw = new FileWriter("cartaoSus.txt", true);
    fw.write(cardSus + "\n");
    fw.close();

    JOptionPane.showMessageDialog(null, "Cartão do SUS salvo com sucesso!");
    cardSusTextArea.setText("");

} catch (Exception e) {
    JOptionPane.showMessageDialog(null,
            "Erro ao salvar:\n" + e.getMessage());
}
                       

    }//GEN-LAST:event_salvarCardSusButtonActionPerformed

