private void carregarCardSus() {
    try {
        InfoDAO dao = new InfoDAO();
        List<String> cartaoSus = dao.listarCardSus();

        cardSusTextArea.setText("");

        for (String c : cartaoSus) {
            cardSusTextArea.append(c + "\n");
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}




