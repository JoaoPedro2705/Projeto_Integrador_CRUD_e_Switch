    private void salvarCpfButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarCpfButtonActionPerformed
      String cpf = cpfTextArea.getText().trim();

    if (cpf.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Digite um CPF!");
        return;
    }

    try {
        
        InfoDAO dao = new InfoDAO();
    dao.inserirCpf(cpf);

    FileWriter fw = new FileWriter("cpf.txt", true);
    fw.write(cpf + "\n");
    fw.close();

    JOptionPane.showMessageDialog(null, "CPF salvo com sucesso!");
    cpfTextArea.setText("");

} catch (Exception e) {
    JOptionPane.showMessageDialog(null,
            "Erro ao salvar:\n" + e.getMessage());
}
    }//GEN-LAST:event_salvarCpfButtonActionPerformed

