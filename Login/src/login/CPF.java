 private void carregarCpf() {
    try {
        InfoDAO dao = new InfoDAO();
        List<String> cpf = dao.listarCpf();

        cpfTextArea.setText("");

        for (String cf : cpf) {
            cpfTextArea.append(cf + "\n");
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}

