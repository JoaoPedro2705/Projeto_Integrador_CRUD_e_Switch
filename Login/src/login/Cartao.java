    private void salvarCartaoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarCartaoButtonActionPerformed
        String cartao = cartaoTextArea.getText().trim();

    if (cartao.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Digite um Numero de cartão!");
        return;
    }

    try {
        
        InfoDAO dao = new InfoDAO();
    dao.inserirCartao(cartao);

    FileWriter fw = new FileWriter("cartao.txt", true);
    fw.write(cartao + "\n");
    fw.close();

    JOptionPane.showMessageDialog(null, "Numero de cartão salvo com sucesso!");
    cartaoTextArea.setText("");
    
    }catch (Exception e) {
    JOptionPane.showMessageDialog(null,
            "Erro ao salvar:\n" + e.getMessage());
    }
    }//GEN-LAST:event_salvarCartaoButtonActionPerformed

