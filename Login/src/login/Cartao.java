private void carregarCartao() {
    try {
        InfoDAO dao = new InfoDAO();
        List<String> cartao = dao.listarCartao();

        cartaoTextArea.setText("");

        for (String ca : cartao) {
            cartaoTextArea.append(ca + "\n");
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}

